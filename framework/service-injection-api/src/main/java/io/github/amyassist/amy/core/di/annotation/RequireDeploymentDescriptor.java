package io.github.amyassist.amy.core.di.annotation;

import static java.lang.annotation.ElementType.*;
import static java.lang.annotation.RetentionPolicy.*;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Mark an annotation, that also require the annotated element to be in a deployment descriptor. Without the deployment
 * descriptor the annotation has no effect. A annotation processor can be used to process this meta annotation and
 * generate the descriptor.
 * 
 * @author Leon Kiefer
 */
@Documented
@Retention(RUNTIME)
@Target(ANNOTATION_TYPE)
public @interface RequireDeploymentDescriptor {

}
